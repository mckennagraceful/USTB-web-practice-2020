<script>
	export default {
		data() {
			return {
				login: false,
				avatarUrl: "../../static/uni-center/logo.png",
				uerInfo: {}
			}
		},
		methods: {
			goLogin:function() {
				if (!this.login) {
					console.log("点击前往登录")
					//页面的跳转
					uni.navigateTo({
						url: "../login/login"
					})
				}
			},
			goToSendMsg: function() {
				uni.navigateTo({
					url: '../case/msg'
				});
			},
			CallApiCase:function(){
				//页面的跳转
				uni.navigateTo({
					url:'../case/api'
				})
			},
			initApp: function() {
				uni.showModal({
					title: '确定要初始化系统吗?',
					content: '初始化系统会清除所有缓存数据并且重启',
					success: function(res) {
						if (res.confirm) {
							console.log('用户点击确定');
							// 清除缓存
							uni.clearStorage();
							// 一秒后重启
							uni.showToast({
								icon: 'none',
								duration: 3000,
								title: '清除成功 1秒后重启'
							});
							setTimeout(function() {
								uni.hideToast();
								plus.runtime.restart();
							}, 1000);
						} else if (res.cancel) {
							console.log('用户点击取消');
						}
					}
				});
			}
		}
	}
</script>

